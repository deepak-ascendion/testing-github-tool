public class AddNumbers {
    public static void main(String[] args) {
        // Define two numbers
        int num1 = 20;
        int num2 = 5;
        
        // Add the numbers
        int sum = num1 + num2;
        
        // Display the result
        System.out.println("First number: " + num1);
        System.out.println("Second number: " + num2);
        System.out.println("Sum: " + sum);
    }
}